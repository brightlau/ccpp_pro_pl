// 在 C 和 C++中，指针有多种类型，主要包括以下几类：

// **一、基本数据类型指针**

// 1. 指向整数的指针：
//    - 例如 `int
//    *ptr`，用于存储整数变量的地址。可以通过这个指针访问和修改所指向的整数变量的值。
// 2. 指向字符的指针：
//    - 如 `char *str`，常用来处理字符串。字符串在 C 和
//    C++中实际上是字符数组，而字符指针可以指向这个字符数组的首地址。
// 3. 指向浮点数的指针：
//    - `float *fp`，用于存储浮点数变量的地址，可操作所指向的浮点数变量。

// **二、指针指向指针（二级指针及更高层级）**

// 1. 二级指针：
//    - 例如 `int
//    **ptr`，这是一个指向整数指针的指针。它可以存储另一个指针的地址，而那个指针又指向一个整数变量。常用于处理动态分配的二维数组等情况。
// 2. 更高层级的指针：
//    -
//    可以有三级指针、四级指针等，但其概念是类似的，每一级指针都指向它的上一级指针类型。随着层级的增加，理解和使用的难度也会相应增加。

// **三、函数指针**

// 1. 定义：
//    - 例如 `int (*funcPtr)(int,
//    int)`，这是一个指向函数的指针。它可以存储函数的地址，通过这个指针可以调用所指向的函数。
// 2. 用途：
//    -
//    常用于实现回调函数，将一个函数作为参数传递给另一个函数，以便在特定的情况下调用。也可用于实现动态的函数调用，根据不同的条件调用不同的函数。

// **四、数组指针和指针数组**

// 1. 数组指针：
//    - 如 `int
//    (*arrayPtr)[5]`，是指向一个包含特定数量元素的数组的指针。它指向的是整个数组，而不是数组中的单个元素。
// 2. 指针数组：
//    - `int
//    *arr[5]`，是一个数组，其中的每个元素都是一个指针。这些指针可以指向不同的整数变量或其他数据。

// **五、结构体指针和类指针（在 C++中）**

// 1. 结构体指针：
//    - 在 C 中，`struct MyStruct
//    *ptr`，用于指向一个结构体变量。可以通过指针访问结构体的成员变量。
//    - 在 C++中，结构体的使用与类类似，结构体指针的行为也类似类指针。
// 2. 类指针：
//    - `class MyClass
//    *objPtr`，用于指向一个类的对象。可以通过指针调用类的成员函数和访问成员变量。

// **六、常量指针和指针常量**

// 1. 常量指针：
//    - 指向常量的指针，例如 `const int
//    *ptr`。指针本身可以修改指向不同的地址，但不能通过这个指针修改所指向的常量值。
// 2. 指针常量：
//    - 指针本身是常量，例如 `int * const
//    ptr`。指针一旦初始化后就不能再指向其他地址，但可以通过指针修改所指向的变量的值。

// **七、空指针**

// 1. 表示：
//    - 在 C 和 C++中，空指针通常用 `NULL`（在 C 中）或 `nullptr`（在
//    C++中）表示。
// 2. 含义：
//    -
//    空指针不指向任何有效的内存地址。它用于表示指针没有被初始化或者指向一个无效的地址。在进行指针操作时，需要检查指针是否为空指针，以避免访问无效内存导致的错误。
int pointers_fun() {
  /*
  指针 从内到外解析
  int *g(int n)：函数，返回 int * 的函数
  int (*g)(int n)：函数指针，指向返回 int 的函数
  int *(*g)(int n)：函数指针，指向返回 int * 的函数
  int (*(*g)(int n))[n]：函数指针，指向返回指向 n 个 int 元素数组的指针的函数
  */
  int *g(int n);
  int (*g1)(int n);
  int *(*g2)(int n);
  int(*(*g3)(int n))[10];
  int *(*(*g4)(int n))[10];
  return 0;
}